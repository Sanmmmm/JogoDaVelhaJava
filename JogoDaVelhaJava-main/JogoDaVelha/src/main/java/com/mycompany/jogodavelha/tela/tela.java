    private void atualizarJogadorAtual() {
        jogadorAtual = jogadorAtual.equals("X") ? "O" : "X";
    }

    private void marcarBotao(JButton botao) {
        if (botao.getText().equals("")) {
            botao.setText(jogadorAtual);
            if (verificarVitoria()) {
                JOptionPane.showMessageDialog(this, "Jogador " + jogadorAtual + " venceu!");
            } else if (verificarEmpate()) {
                JOptionPane.showMessageDialog(this, "Empate!");
            } else {
                atualizarJogadorAtual();
            }
        }
    }

    private boolean verificarVitoria() {
    // Verifica todas as linhas, colunas e diagonais
    return (verificarSequencia(jButton7, jButton8, jButton9) || // Linha superior
            verificarSequencia(jButton4, jButton5, jButton6) || // Linha do meio
            verificarSequencia(jButton2, jButton3, jButton10) || // Linha inferior
            verificarSequencia(jButton7, jButton4, jButton2) || // Coluna esquerda
            verificarSequencia(jButton8, jButton5, jButton3) || // Coluna do meio
            verificarSequencia(jButton9, jButton6, jButton10) || // Coluna direita
            verificarSequencia(jButton7, jButton5, jButton10) || // Diagonal principal
            verificarSequencia(jButton9, jButton5, jButton2));   // Diagonal secundária
}


    private boolean verificarSequencia(JButton b1, JButton b2, JButton b3) {
        return b1.getText().equals(jogadorAtual) &&
               b1.getText().equals(b2.getText()) &&
               b2.getText().equals(b3.getText()) &&
               !b1.getText().equals("");
    }

    private boolean verificarEmpate() {
        // Verifica se todos os botões estão preenchidos
        return !jButton2.getText().equals("") &&
               !jButton3.getText().equals("") &&
               !jButton4.getText().equals("") &&
               !jButton5.getText().equals("") &&
               !jButton6.getText().equals("") &&
               !jButton7.getText().equals("") &&
               !jButton8.getText().equals("") &&
               !jButton9.getText().equals("") &&
               !jButton10.getText().equals("");
    }

    private void limparTabuleiro() {
        
        jButton2.setText("");
        jButton3.setText("");
        jButton4.setText("");
        jButton5.setText("");
        jButton6.setText("");
        jButton7.setText("");
        jButton8.setText("");
        jButton9.setText("");
        jButton10.setText("");
        jogadorAtual = "X";
    }
                                        

